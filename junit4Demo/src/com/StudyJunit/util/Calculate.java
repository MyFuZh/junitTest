package com.StudyJunit.util;

public class Calculate {
	
	public int and(int a,int b){
		return a+b;
	}
	
	public int subtract(int a,int b){
		return a-b;
	}
	
	public int multiplication(int a,int b){
		return a*b;
	}
	

	public int except(int a,int b){
		return a/b;
	}



}
